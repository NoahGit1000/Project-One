public class MyArrayList {

	public static void printMyArray(int[] a) {
		
		System.out.print("Array integerArray contains:\n");
		
		for (int b : a)
			System.out.print(b + " ");
		
	}
	
	public static void printMyArray(double[] a) {
		
		System.out.print("Array doubleArray contains:\n");
		
		for (double b : a)
			System.out.print(b + " ");
		
	}
	
	public static void printMyArray(char[] a) {
		
		System.out.print("Array characterArray contains:\n");
		
		for (char b : a)
			System.out.print(b + " ");
		
	}
	
}
